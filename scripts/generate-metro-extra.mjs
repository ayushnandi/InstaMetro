// Generates src/data/metroExtra.generated.ts from the ground-truth CSV
// (E:\Apps\Delhi_Metro_Complete_303.csv), supplying fields dmrc.ts doesn't carry:
// opening date, interchange line names, platform directions, distance/travel time.
// Re-run after the CSV changes: node scripts/generate-metro-extra.mjs
import { readFileSync, writeFileSync } from 'node:fs';
import { STATIONS } from '../src/data/dmrc.ts';

const CSV_PATH = 'E:\\Apps\\Delhi_Metro_Complete_303.csv';

function parseCsvLine(line) {
  // Fields never contain commas inside quotes in this file — plain split is safe.
  return line.split(',');
}

function normalize(name) {
  return name.toLowerCase().replace(/[^a-z0-9]/g, '');
}

function parseDateDMY(s) {
  const [d, m, y] = s.split('-');
  return `${y}-${m}-${d}`; // ISO, sortable
}

const raw = readFileSync(CSV_PATH, 'utf8').split('\n').filter(Boolean);
const header = parseCsvLine(raw[0]);
const rows = raw.slice(1).map(line => {
  const cols = parseCsvLine(line);
  const rec = {};
  header.forEach((h, i) => { rec[h.trim()] = (cols[i] ?? '').trim(); });
  return rec;
});

// Group CSV rows by normalized station name (a station appears once per line it serves).
const byName = new Map();
for (const row of rows) {
  const key = normalize(row.Station_Name);
  const arr = byName.get(key) ?? [];
  arr.push(row);
  byName.set(key, arr);
}

// dmrc.ts uses slightly different display names than the raw CSV for a few stations.
const NAME_ALIASES = {
  'Anand Vihar ISBT': 'Anand Vihar',
  'IIT Delhi': 'IIT',
  'IGI Airport T2/T3': 'Indira Gandhi International Airport',
};

const extra = {};
const unmatched = [];

for (const station of STATIONS) {
  const key = normalize(NAME_ALIASES[station.name_en] ?? station.name_en);
  const rows = byName.get(key);
  if (!rows || rows.length === 0) {
    unmatched.push(station.name_en);
    continue;
  }

  const opened = rows
    .map(r => r.Opened)
    .filter(Boolean)
    .sort((a, b) => parseDateDMY(a).localeCompare(parseDateDMY(b)))[0] ?? null;

  const interchangeLines = [
    ...new Set(
      rows.flatMap(r => (r.Interchange_Lines || '').split(';').map(s => s.trim()).filter(Boolean))
    ),
  ];

  const notes = [...new Set(rows.map(r => r.Notes).filter(Boolean))];

  extra[station.id] = {
    opened,
    layout: rows[0].Layout || null,
    platformType: rows[0].Platform_Type || null,
    interchangeLines,
    notes,
    distFromLineStartKm: rows.map(r => Number(r.Dist_From_Line_Start_km)).filter(n => !Number.isNaN(n)),
  };
}

if (unmatched.length > 0) {
  console.warn(`generate-metro-extra: ${unmatched.length} station(s) had no CSV match:`, unmatched);
}

const out = `// AUTO-GENERATED by scripts/generate-metro-extra.mjs from Delhi_Metro_Complete_303.csv — do not edit by hand.
export type StationExtra = {
  opened: string | null;
  layout: string | null;
  platformType: string | null;
  interchangeLines: string[];
  notes: string[];
  distFromLineStartKm: number[];
};

export const STATION_EXTRA: Record<string, StationExtra> = ${JSON.stringify(extra, null, 2)};
`;

writeFileSync(new URL('../src/data/metroExtra.generated.ts', import.meta.url), out);
console.log(`generate-metro-extra: wrote ${Object.keys(extra).length} stations.`);
